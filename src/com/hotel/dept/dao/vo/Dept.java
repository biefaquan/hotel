package com.hotel.dept.dao.vo;

public class Dept {
	private int d_id;
	private String d_name;  //部门名称
	/**
	 * @return the d_id
	 */
	public int getD_id() {
		return d_id;
	}
	/**
	 * @param d_id the d_id to set
	 */
	public void setD_id(int d_id) {
		this.d_id = d_id;
	}
	/**
	 * @return the d_name
	 */
	public String getD_name() {
		return d_name;
	}
	/**
	 * @param d_name the d_name to set
	 */
	public void setD_name(String d_name) {
		this.d_name = d_name;
	}
	
}
