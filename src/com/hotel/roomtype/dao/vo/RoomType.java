package com.hotel.roomtype.dao.vo;

public class RoomType {
	private int rt_id;
	private String rt_name;   //房型名称
	private int rt_price;     //价格
	private int rt_zdprice;   //钟点房价格
	private int rt_zdstate;   //钟点房状态
	private int rt_num;       //床位数
	private String rt_remark;  //备注
	/**
	 * @return the rt_id
	 */
	public int getRt_id() {
		return rt_id;
	}
	/**
	 * @param rt_id the rt_id to set
	 */
	public void setRt_id(int rt_id) {
		this.rt_id = rt_id;
	}
	/**
	 * @return the rt_name
	 */
	public String getRt_name() {
		return rt_name;
	}
	/**
	 * @param rt_name the rt_name to set
	 */
	public void setRt_name(String rt_name) {
		this.rt_name = rt_name;
	}
	/**
	 * @return the rt_price
	 */
	public int getRt_price() {
		return rt_price;
	}
	/**
	 * @param rt_price the rt_price to set
	 */
	public void setRt_price(int rt_price) {
		this.rt_price = rt_price;
	}
	/**
	 * @return the rt_zdprice
	 */
	public int getRt_zdprice() {
		return rt_zdprice;
	}
	/**
	 * @param rt_zdprice the rt_zdprice to set
	 */
	public void setRt_zdprice(int rt_zdprice) {
		this.rt_zdprice = rt_zdprice;
	}
	/**
	 * @return the rt_zdstate
	 */
	public int getRt_zdstate() {
		return rt_zdstate;
	}
	/**
	 * @param rt_zdstate the rt_zdstate to set
	 */
	public void setRt_zdstate(int rt_zdstate) {
		this.rt_zdstate = rt_zdstate;
	}
	/**
	 * @return the rt_num
	 */
	public int getRt_num() {
		return rt_num;
	}
	/**
	 * @param rt_num the rt_num to set
	 */
	public void setRt_num(int rt_num) {
		this.rt_num = rt_num;
	}
	/**
	 * @return the rt_remark
	 */
	public String getRt_remark() {
		return rt_remark;
	}
	/**
	 * @param rt_remark the rt_remark to set
	 */
	public void setRt_remark(String rt_remark) {
		this.rt_remark = rt_remark;
	}
	
	
}
