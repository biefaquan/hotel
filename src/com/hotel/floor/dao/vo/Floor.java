package com.hotel.floor.dao.vo;

public class Floor {
	private int f_id;
	private int f_num;  //楼层号
	/**
	 * @return the f_id
	 */
	public int getF_id() {
		return f_id;
	}
	/**
	 * @param f_id the f_id to set
	 */
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	/**
	 * @return the f_num
	 */
	public int getF_num() {
		return f_num;
	}
	/**
	 * @param f_num the f_num to set
	 */
	public void setF_num(int f_num) {
		this.f_num = f_num;
	}
	
}
